# Declare an array with 4 elements
array=( Harshita Radhika Vidhita Rudrakshi )

# Print the entire array
echo "Entire array: ${array[@]}"

# Print the first element of the array
echo "First element: ${array[0]}"

# Print the second element of the array
echo "Second element: ${array[1]}"

# Print the third element of the array
echo "Third element: ${array[2]}"

# Print the fourth element of the array
echo "Fourth element: ${array[3]}"

# Print the length of the array
echo "Array length: ${#array[@]}"



